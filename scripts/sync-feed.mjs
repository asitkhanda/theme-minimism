async function main() {
  console.log(`Fetching ${feedUrl}...`);
  const response = await fetch(feedUrl);
  if (!response.ok) {
    throw new Error(`Failed to fetch feed: ${response.status}`);
  }

  const feed = await response.json();
  const themeConfig = existsSync(themeConfigPath)
    ? JSON.parse(readFileSync(themeConfigPath, "utf8"))
    : {};

  clearGeneratedPosts();
  for (const item of feed.items ?? []) {
    writePost(item);
  }

  writeAboutPage(feed.title);
  writeHugoConfig(feed, themeConfig);

  console.log(`Synced ${feed.items?.length ?? 0} posts to dev/content/post/`);
  console.log("Updated dev/config.toml with accent colors from config.json");
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
