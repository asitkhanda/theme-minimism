#!/usr/bin/env node

import {
  existsSync,
  mkdirSync,
  readFileSync,
  readdirSync,
  rmSync,
  writeFileSync,
} from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..");
const devDir = path.join(root, "dev");
const contentPostDir = path.join(devDir, "content", "post");
const feedUrl = process.env.FEED_URL ?? "https://asit.blog/feed.json";
const baseUrl =
  process.env.BASE_URL ??
  process.env.HUGO_BASEURL ??
  "http://localhost:1313/";
const themeConfigPath = path.join(root, "config.json");

function parseUrlParts(itemUrl) {
  const { pathname } = new URL(itemUrl);
  const match = pathname.match(/^\/(\d{4})\/(\d{2})\/(\d{2})\/(.+)\.html$/);
  if (!match) {
    throw new Error(`Unexpected post URL format: ${itemUrl}`);
  }

  const [, year, month, day, slug] = match;
  return { year, month, day, slug, pathname };
}

function clearGeneratedPosts() {
  if (!existsSync(contentPostDir)) {
    mkdirSync(contentPostDir, { recursive: true });
    return;
  }

  for (const year of readdirSync(contentPostDir)) {
    const yearPath = path.join(contentPostDir, year);
    rmSync(yearPath, { recursive: true, force: true });
  }
}

function writePost(item) {
  const { pathname, year, month, day, slug } = parseUrlParts(item.url);
  const dir = path.join(contentPostDir, year, month, day);
  mkdirSync(dir, { recursive: true });

  const frontMatter = [
    "---",
    item.title ? `title: ${JSON.stringify(item.title)}` : 'title: ""',
    `date: ${item.date_published}`,
    `url: ${pathname}`,
    "type: post",
    "draft: false",
    "---",
    "",
  ].join("\n");

  const body = item.content_html?.trim() ?? "";
  writeFileSync(path.join(dir, `${slug}.md`), `${frontMatter}${body}\n`);
}

function writeAboutPage(siteTitle) {
  const aboutDir = path.join(devDir, "content", "about");
  mkdirSync(aboutDir, { recursive: true });
  writeFileSync(
    path.join(aboutDir, "_index.md"),
    `---
title: About
---

# ${siteTitle}

Local preview page. Edit this file or export content from Micro.blog for your real about page.
`,
  );
}

